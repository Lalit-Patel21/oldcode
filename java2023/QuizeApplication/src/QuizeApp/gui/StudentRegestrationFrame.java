    private void btnStudentRegesterActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnStudentRegesterActionPerformed
        // TODO add your handling code here:
        if(validateInputs()){
        StudentPojo student=new StudentPojo();
        student.setStudentEmail(txtStudentEmail.getText().trim());
        student.setStudentName(txtStudentName.getText().trim());
        student.setStudentPassword(String.valueOf(txtStudentPassword.getPassword()));
        try{
             if(StudentDao.addStudent(student)){
                 JOptionPane.showMessageDialog(null,"Student Regestered!");
                 Mailer.sendMailToStudent(txtStudentName.getText().trim(),txtStudentEmail.getText().trim(),String.valueOf(txtStudentPassword.getPassword()));
                 JOptionPane.showMessageDialog(null,"Mail Sent Succesfully to"+txtStudentEmail.getText().trim());
                 return;
              }
                JOptionPane.showMessageDialog(null,"Cant Regester Student!");
        }catch(SQLException sq){
            JOptionPane.showMessageDialog(null,"DB Error in Student Regester Frame");
            return;
        }catch(MessagingException ms){
            JOptionPane.showMessageDialog(null,"Mail Error in Student Login Frame");
            return;
        }
        }
    }//GEN-LAST:event_btnStudentRegesterActionPerformed


    private boolean validateInputs() {
        if((txtStudentName.getText().trim().isEmpty()||txtStudentName.getText().trim().isEmpty()||String.valueOf(txtStudentPassword.getPassword()).trim().isEmpty())){
            JOptionPane.showMessageDialog(null,"Please Fill All The Field");
            return false;
        }
        if(!Validator.isValidEmail(txtStudentEmail.getText().trim())){
            JOptionPane.showMessageDialog(null,"Please Enter Valid Email");
            return false;
        }
        return true;
    }

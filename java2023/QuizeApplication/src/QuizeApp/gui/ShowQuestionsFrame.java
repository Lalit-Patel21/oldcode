    private void loadQuetions() {
        try{
             questions=QuestionDao.getAllQuestions("ACTIVE");
             q1=questions.get(i);
             txtque.setText(q1.getQuestion());
             o1.setText(q1.getOption1());
             o2.setText(q1.getOption2());
             o3.setText(q1.getOption3());
             o4.setText(q1.getOption4());
             if(questions.isEmpty()){
                  JOptionPane.showMessageDialog(null,"Checked All Questions");
                  return;
             }
        }catch(SQLException sq){
            JOptionPane.showMessageDialog(null,"DB Error in Attempt quiz");
            return;
        }
    }
    
    private void showQuestions() {
        if(questions.isEmpty()){
                  JOptionPane.showMessageDialog(null,"Attempted All Quetions");
             }
             q1=questions.get(i);
             txtque.setText(q1.getQuestion());
             o1.setText(q1.getOption1());
             o2.setText(q1.getOption2());
             o3.setText(q1.getOption3());
             o4.setText(q1.getOption4());
    }

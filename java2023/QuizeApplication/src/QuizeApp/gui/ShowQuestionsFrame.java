    private void removeActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_removeActionPerformed
        // TODO add your handling code here:
        try{
            if(QuestionDao.RemoveQuestions(q1.getQuestionId(),"ACTIVE")){
                JOptionPane.showMessageDialog(null,"Question Removed!");
                questions.remove(i);
                i++;
                showQuestions();
                return;
            }
            JOptionPane.showMessageDialog(null,"Cant Removed Question!");
        }catch(SQLException sq){
            JOptionPane.showMessageDialog(null,"DB Error in Attempt quiz");
            return;
        }
    }//GEN-LAST:event_removeActionPerformed

